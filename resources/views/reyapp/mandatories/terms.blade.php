<h1>Condiciones y políticas.</h1>

<h2>Compañías.</h2>

<p>Cualquier persona física o moral que cuente con un espacio físico adaptado para la óptima ejecución musical de solistas, artistas y/o grupos (Desde ahora nombrado como Salas de Ensayo) podrá inscribirse en ensayopro.com.mx, sólo deberá de llenar el formulario titulado "Compañía" con sus datos de contacto, dirección completa, CLABE interbancaria, Nombre del titular de la cuenta y banco emisor. Anexar la información de la o las salas de ensayo a ofrecer incluyendo nombres de identificación de cada sala, descripción, fotografías legibles con medidas máximas de 1000 x 1000 pixeles y mínimas de 500 x 500 pixeles con una resolución de 72 ppp del espacio y/o video demostrativo. </p>

<p>Se podrán ingresar el número de salas necesarias a nombre de la empresa cuya dirección no sea diferente a la ingresada en el formulario de registro, en caso de que las salas de ensayo se encuentren en otra dirección, se podrá incluir la dirección deseada en el formulario de registro de sala.</p>

<p>Las compañías ingresadas en ensayopro.com.mx se comprometen a cumplir con los más altos estándares de calidad en servicio, limpieza e instrumentación y con ello ofrecer precios competitivos a los usuarios de ensayopro.com.mx</p>

<p>Con la finalidad de mantener un servicio web de alta calidad y para poder ofrecer beneficios exclusivos por parte de ensayopro.com.mx, se cobrarán las siguientes:</p>

<h3>Comisiones por servicio de ensayopro.com.mx.</h3>

<p>La comisión por el servicio será del 10% la cual se calcula a partir del subtotal bruto de la reservación (antes de impuestos) y se restada automáticamente del pago a recibir.</p>

<h2>Sobre los costos.</h2>

<p>Los costos por hora no podrán ser menores a $50.00 con la finalidad de ofrecer precios competitivos.</p>

<p>Los costos de las salas ofrecidos por las compañías en ensayopro.com.mx no podrán ser mayores a los ofrecidos fuera de la plataforma.</p>

<p>Cancelaciones por parte de las compañías.</p>


<p>Las cancelaciones por desastre natural, emergencia o inhabilitación temporal de las o las salas de ensayo se deberán de cambiar de fecha previa autorización del músico enviando un correo a webmaster@ensayopro.com.mx, en caso de que la sala de ensayo se encuentre inhabilitada de forma permanente se realizará un reembolso del total de las horas apartadas por el músico.</p>

<h2>Pagos.</h2>

<p>Los pagos por parte de ensayopro.com.mx a las compañías se realizarán de forma semanal al número de CLABE ingresado en el formulario de registro de la compañía.
Ensayopro.com.mx no se hace responsable por el mal manejo del número de CLABE, por lo que la compañía se compromete a verificar que el número CLABE sea el correcto.</p>

<h2>Cuenta de empresas en ensayopro.com.mx</h2>

<p>Cada compañía contará con un dashboard donde podrá visualizar las reservaciones realizadas por los usuarios y contará con una cuenta de administrador.</p>

<p>Las compañías serán responsables del uso de su cuenta. El correo y contraseña utilizados deberán de ser controlados por los administradores de cada compañía. Ensayopro.com.mx no se hace responsable por el mal uso de estos datos por parte de los administradores de las compañías.</p>

<p>En caso de olvidar la contraseña, el administrador deberá de elegir la sección "olvide mi contraseña" en el formulario de acceso. Te enviaremos un correo electrónico con un link para que puedas cambiar tu contraseña. En caso de no contar con acceso a tu cuenta de correo electrónico, el administrador deberá de enviar un correo a webmaster@ensayopro.com.mx con la solicitud para cambiar la contraseña de la cuenta adjuntando una identificación oficial del administrador y una identificación de la compañía (comprobantes de pago, RFC, Acta constitutiva, etc.) Ensayo Pro puede solicitar algún otro documento que no se encuentre mencionado si detecta algún tipo de fraude en la cuenta y podrá ponerse en contacto con el administrador a los números de contacto guardados en la plataforma.</p>

<p>Las compañías cuentan con un calendario automatizado para llevar el control de las reservaciones hechas por sistema o manualmente.</p>

<h2>Músico.</h2>

<p>Cualquier persona física o moral que desee rentar un espacio físico adaptado para la óptima ejecución musical podrán inscribirse en ensayopro.com.mx, sólo deberá de llenar el formulario de registro con sus datos de contacto y de identificación.</p>

<p>Ensayopro.com.mx mostrará en la sección "Agenda tu Ensayo" las Salas de ensayo inscritas por medio de las empresas a las que se encuentran afiliadas. El Músico podrá ingresar a la información de cada Sala de Ensayo y elegir la que más le convenga de acuerdo a localización, costos y calificaciones.</p>

<h3>Sobre los costos y promociones de las Salas de Ensayo.</h3>

<p>Cada Sala de Ensayo será parte de las compañías que se han inscrito en ensayopro.com.mx por lo que los costos y promociones serán responsabilidad de las mismas.</p>

<h2>Sobre las Calificaciones a las Salas de Ensayo.</h2>

<p>Cada músico podrá calificar a las Salas de Ensayo con la finalidad de mejorar los servicios de las mismas. Dicha calificación se mostrará mediante el sistema de "estrellas" donde 5 es la mejor calificación y 1 es la peor. </p>

<h2>Comentarios hacia las Salas de Ensayo.</h2>

<p>ensayopro.com.mx se encargará de administrar los comentarios por lo que el uso de malas palabras o comentarios que no sean en favor de la sana competencia entre los participantes serán invalidados y por lo tanto no estarán a la vista del público.</p>

<h2>Agendar ensayos.</h2>

<p>Un ensayo quedará agendado en cuanto se haga el pago completo por la misma mediante el sistema de pagos en tiendas oxxo por el total del monto final.
El Músico contará con un periodo de 24 horas para concluir el pago correspondiente, mientras tanto el horario seleccionado quedará bloqueado. En caso de no realizar el pago dentro de las 24 horas de pago, el ensayo quedará cancelado de forma automática.</p>

<h2>Cancelaciones.</h2> 

<p>En caso de no poder concluir con el pago en el periodo de las 24 horas asignadas, el músico deberá de cancelar el ensayo.</p>

<p>En caso de haber concluido con el pago de las horas contratadas y el Músico necesite cancelar el ensayo, éste deberá de realizarlo 24 horas antes de la fecha mediante el botón de cancelación y el sistema permitirá re-agenar la fecha. Si el ensayo se cancela después de las 24 horas antes del ensayo éste quedará cancelado y no se podrá re-agendar ni hacer devolución del dinero.</p>

<p>Ejemplo: Tu ensayo es el lunes a las 13 horas y cancelas el domingo a las 12 horas (se re-agenda)
Tu ensayo es el lunes a las 13 horas y cancelas el domingo a las 21 horas (no se re-agenda)</p>


	
